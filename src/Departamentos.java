public class Departamentos {
    public String idDep;
    public String nombreDep;

    public Departamentos(String idDep, String nombre) {
        this.idDep = idDep;
        this.nombreDep = nombreDep;
    }

    public String getIdDep() {
        return idDep;
    }

    public String getNombreDep() {
        return nombreDep;
    }
}